package com.example.second;

public class NToThePowerOf {

    public static void main(String[] args) {
        // input
        int n= 5;
        int power = 0;

        // solution
        int result = 1;

        for(int a=1;a<=power;a++){
            result = result * n;
        }
        System.out.println(result);




    }

}
